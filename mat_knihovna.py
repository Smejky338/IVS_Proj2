import math

def Plus(a, b):
    return a+b

def Minus(a, b):
    return a-b

def Multiply(a, b):
    return a*b

def Divide(a, b):
    return a/b

def Factorial(a):
    return math.factorial(a)

def Power(x, y):
    return x ** y

def Odmocnina(x, y):
    return x ** (1/y)

def Log(x):
    return math.log10(x)



